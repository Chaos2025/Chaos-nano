/*=================OLED=========*/
/*=========写指令,高位在前 =====*/
void OLED_WrCmd(u8 cmd)
{
  u8 i=8;	

  OLED_DC_CLR();
  OLED_SCL_CLR();  

  while(i--)
  {
    if(cmd&0x80){OLED_SDA_SET();}
    else{OLED_SDA_CLR();}
    OLED_SCL_SET();             
    OLED_SCL_CLR();    
    cmd<<=1;;   
  } 	
}

/*===写数据，inverse取反标志位,1表示取反（背景反色）===*/
void OLED_WrDat(u8 data,u8 inverse)
{
  u8 i=8;
  OLED_DC_SET();
  OLED_SCL_CLR(); 
  if(inverse==1)
   data=~data;  
  while(i--)
  {
    if(data&0x80){OLED_SDA_SET();}
    else{OLED_SDA_CLR();}
    OLED_SCL_SET();             
    OLED_SCL_CLR();    
    data<<=1;    
  }
}

/*-------------------------------------------
【功 能 】:设置地址 地址设置为PAGE寻址模式下

【说 明 】:先写低四位地址，写高四位地址 其高低位
           不分先后
           X为起始列 其高低四位分开写 
           Y为PAGE值[0---7]
*********************************************/
void OLED_Set_Pos(u8 x, u8 y)
{ 
  OLED_WrCmd(pageadd+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f)|0x01); 
} 


/*==========================清屏=========================*/
void OLED_CLS(void)
{
  u8 y,x;	
  for(y=0;y<8;y++)
  {
    OLED_WrCmd(0xb0+y);
    OLED_WrCmd(0x01);
    OLED_WrCmd(0x10); 
    for(x=0;x<X_WIDTH;x++)
      OLED_WrDat(0,0);
  }
}

